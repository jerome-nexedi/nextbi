<?php

/**
 * Specifies the text orientation.
 * @package wsscharts
 * @subpackage enums
 **/
class XlOrientation {

	/**
	 * Text runs downward.
	 * @access public
	 * @var integer
	 **/
	const xlDownward = -4170;

	/**
	 * Text runs horizontally.
	 * @access public
	 * @var integer
	 **/
	const xlHorizontal = -4128;

	/**
	 * Text runs upward.
	 * @access public
	 * @var integer
	 **/
	const xlUpward = -4171;

	/**
	 * Text runs downward and is centered in the cell.
	 * @access public
	 * @var integer
	 **/
	const xlVertical = -4166;

}

?>