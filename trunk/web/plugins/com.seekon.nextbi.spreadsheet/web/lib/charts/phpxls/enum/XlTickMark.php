	/**
	 * No mark.
	 * @access public
	 * @var integer
	 **/
	const xlTickMarkNone = -4142;

	/**
	 * Outside the axis.
	 * @access public
	 * @var integer
	 **/
	const xlTickMarkOutside = 3;

}

