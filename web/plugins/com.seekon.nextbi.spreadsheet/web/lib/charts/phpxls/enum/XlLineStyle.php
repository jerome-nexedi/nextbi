<?php

/**
 * Specifies the line style for the border.
 * @package wsscharts
 * @subpackage enums
 **/
class XlLineStyle {
	
	/**
	 * Continuous line.
	 * @access public
	 * @var integer
	 **/
	const xlContinuous = 1;
	
	/**
	 * Dashed line.
	 * @access public
	 * @var integer
	 **/
	const xlDash = -4115;
	
	/**
	 * Alternating dashes and dots.
	 * @access public
	 * @var integer
	 **/
	const xlDashDot = 4;
	
	/**
	 * Dash followed by two dots.
	 * @access public
	 * @var integer
	 **/
	const xlDashDotDot = 5;
	
	/**
	 * Dotted line.
	 * @access public
	 * @var integer
	 **/
	const xlDot = -4118;
	
	/**
	 * Double line.
	 * @access public
	 * @var integer
	 **/
	const xlDouble = -4119;
	
	/**
	 * No line.
	 * @access public
	 * @var integer
	 **/
	const xlLineStyleNone = -4142;
	
	/**
	 * Slanted dashes.
	 * @access public
	 * @var integer
	 **/
	const xlSlantDashDot = 13; 

}
	
